package model;

import lombok.Getter;
import lombok.Setter;

/**
 *
 * @author VINH
 */
@Setter
@Getter
public class Admin {

    private String name;
    private String email;
    private String password;

    public Admin() {
    }

    public Admin(String name, String email, String password) {
        this.name = name;
        this.email = email;
        this.password = password;
    }

}
